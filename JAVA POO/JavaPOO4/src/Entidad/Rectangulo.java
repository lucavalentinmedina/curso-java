package Entidad;

public class Rectangulo {

    private float base;
    private float height;

    public Rectangulo() {
    }

    public Rectangulo(float base, float height) {
        this.base = base;
        this.height = height;
    }

    public float getBase() {
        return base;
    }

    public void setBase(float base) {
        this.base = base;
    }

    public float getHeight() {
        return height;
    }

    public void setHeight(float height) {
        this.height = height;
    }

}
